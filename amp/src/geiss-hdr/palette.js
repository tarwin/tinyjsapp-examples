// (as a Float32Array of RGBA values).
function DeserializePalette(str) {
  const parts = str.trim().split(/\s+/); // split on whitespace

  const n = parseInt(parts[0], 10);

  if (!Number.isFinite(n) || n < 0) {
    throw new Error("Invalid count");
  }

  const out = new Float32Array(n * 4);

  for (let i = 0; i < n; i++) {
    out[i * 4 + 0] = parseFloat(parts[i * 3 + 1]);
    out[i * 4 + 1] = parseFloat(parts[i * 3 + 2]);
    out[i * 4 + 2] = parseFloat(parts[i * 3 + 3]);
    out[i * 4 + 3] = 1.0;
  }

  return out;
}

