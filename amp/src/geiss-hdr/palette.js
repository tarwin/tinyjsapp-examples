export function SetOverridePalette(palette) {
	const N = g_text_palettes.length;
	if (g_palette_override_index < 0 || g_palette_override_index >= N) {
		return;
	}

	g_text_palettes[g_palette_override_index] = SerializePalette(palette);
}

export function GetOverridePalette() {
	const N = g_text_palettes.length;
	if (g_palette_override_index < 0 || g_palette_override_index >= N) {
		return;
	}

	const name = `Palette ${g_palette_override_index}`;
	return { 
		palette : DeserializePalette(g_text_palettes[g_palette_override_index]), 
		name : name
	};
}

export function AdjustOverridePalette(scale, 
                                      adj_r, adj_g, adj_b, 
                                      adj_i) {
	const N = g_text_palettes.length;
	if (g_palette_override_index < 0 || g_palette_override_index >= N) {
		return;
	}

	let p = DeserializePalette(g_text_palettes[g_palette_override_index]);
		
	let i = adj_i * 4;		
	if (i >= p.length) {
		return;
	}

	if (adj_r) p[i + 0] *= scale;
	if (adj_g) p[i + 1] *= scale;
	if (adj_b) p[i + 2] *= scale;

	g_text_palettes[g_palette_override_index] = SerializePalette(p);
}

export function ClearOverridePalette() {
	let override_was_in_use = (g_palette_override_index != -1);
	g_palette_override_index = -1;
	return override_was_in_use;
}

export function OverridePaletteRel(delta) {
	const N = g_text_palettes.length;
	if (g_palette_override_index < 0 || g_palette_override_index >= N) {
		g_palette_override_index = 0;
	} else {
		g_palette_override_index = (g_palette_override_index + delta + N) % N;
	}
}

export function OverridePaletteAbs(index) {
	const N = g_text_palettes.length;
	if (index >= 0 && index < N) {
		g_palette_override_index = index;
	}
}

